public class Demo26 {
    public static void main(String[] args) {
        
        // Conditional AND &&
        // Logical AND &

        // Conditional OR ||
        // Logical OR |

        int x = 10;
        int y = 5;

        boolean result = x == 10 & y < 10; // true & true => true
        System.out.println(result);

        boolean result2 = x == 10 && y < 10; // true & true => true
        System.out.println(result2);

    }
}
